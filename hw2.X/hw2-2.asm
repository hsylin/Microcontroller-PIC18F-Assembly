Initial:
    
MOVLW 0x08
MOVWF 0x001
MOVLW 0x0B
MOVWF 0x10 	
MOVLW 0x05
MOVWF 0x11 	
MOVLW 0x40
MOVWF 0x12 	
MOVLW 0x07
MOVWF 0x13 	
MOVLW 0x0D
MOVWF 0x14 	
MOVLW 0x7F
MOVWF 0x15 
MOVLW 0x0A
MOVWF 0x16	
MOVLW 0x01
MOVWF 0x17 	
MOVLW 0xFE
MOVWF 0x18
    
LFSR 0,0x010
LFSR 1,0x010
LFSR 2,0x011	
MOVLW 0x05

    
for: 
    
    i:
    CPFSLT POSTINC1
    GOTO jump
    GOTO i
    jump:
    DECF INDF1
    INCF POSTDEC1
    
    CLRF WREG
    DECF INDF2
    INCF POSTDEC2
    ADDWF POSTINC2,0
    
   CPFSGT INDF2
   GOTO swap
   CLRF WREG
   ADDWF INDF2,0
   
    swap:
    MOVFF INDF1, 0x02
    MOVFF INDF2, INDF1
    MOVFF 0x02,INDF2
    CLRF 0x02
    CPFSEQ POSTINC1
    GOTO swap
    
   
    CLRF WREG
    ADDWF INDF0,0
    
    ii:
    CPFSEQ POSTDEC1
    GOTO ii
    DECF INDF1
    INCF POSTINC1
    
    CLRF WREG
    ADDWF PREINC2,0

    DECFSZ 0x001
    GOTO for
end


